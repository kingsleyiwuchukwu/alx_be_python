# Python script that converts a specific number of hours into seconds.
# This task reinforces the concept of arithmetic operations within a practical context.

# Declare a variable
hours = 2
#there are 60 minutes in an hour and 60 seconds in a minute, thus 60 x 60 = 3600)
seconds = hours * 3600

# Display the result
print(f"{hours} hour(s) is {seconds} seconds")
