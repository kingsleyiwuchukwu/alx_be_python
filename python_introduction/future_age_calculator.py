# This Python script that asks the user for their current age and then calculates how old they will be in a specific future year.
# This task introduces handling user input and reinforces arithmetic operations.

age = int (input("How old are you? "))

# Calculate how old the user will be in the year 2050.
age = age + 27

# Display the result. 
print(f"In 2050, you will be {age} years old.")