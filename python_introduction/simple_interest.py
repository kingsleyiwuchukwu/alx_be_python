# This a simple Python script that calculates the simple interest earned on an investment over a period of time.
#The formula for simple interest is (I = P * R * T).

# Declared 3 variable
principal = int(1000)
rate = float(0.05)
time = 3

# calculate the simple interest
interest = principal * rate * time

# Display the result.
print(f"The simple interest is: ${interest}")